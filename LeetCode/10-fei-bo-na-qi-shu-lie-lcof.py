# 时间复杂度：O(n)
# 空间复杂度：O(1)
###############################################################################################
class Solution:
    def fib(self, n: int) -> int:
        if n == 0:
            return 0
        elif n == 1:
            return 1
        MOD = int(1e9+7)
        f0, f1, f2 = 0, 1, None
        for _ in range(2, n+1):
            f2 = (f1 + f0) % MOD
            f0 = f1
            f1 = f2
        return f2